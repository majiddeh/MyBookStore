package com.example.mybookstore.Models;

public class ModelAddress {
    int idAddress;
    String name;
    String family;
    String phone;
    String postalcode;
    String city;
    String cityCap;
    String idcityCap;
    String idcity;
    String mobile;
    String address;
    String iduser;

    public ModelAddress() {
    }

    public String getIdcityCap() {
        return idcityCap;
    }

    public void setIdcityCap(String idcityCap) {
        this.idcityCap = idcityCap;
    }

    public String getIdcity() {
        return idcity;
    }

    public void setIdcity(String idcity) {
        this.idcity = idcity;
    }

    public int getIdAddress() {
        return idAddress;
    }

    public void setIdAddress(int idAddress) {
        this.idAddress = idAddress;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFamily() {
        return family;
    }

    public void setFamily(String family) {
        this.family = family;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPostalcode() {
        return postalcode;
    }

    public void setPostalcode(String postalcode) {
        this.postalcode = postalcode;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCityCap() {
        return cityCap;
    }

    public void setCityCap(String cityCap) {
        this.cityCap = cityCap;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getIduser() {
        return iduser;
    }

    public void setIduser(String iduser) {
        this.iduser = iduser;
    }
}
